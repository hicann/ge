void LoadUb2ubAbsStore_BeforeAutofuse(ge::AscGraph &graph) {
  auto s0 = graph.CreateSizeVar("s0");
  auto s1 = graph.CreateSizeVar("s1");
  auto s2 = graph.CreateSizeVar("s2");

  auto z0 = graph.CreateAxis("z0", s0);
  auto z1 = graph.CreateAxis("z1", s1);
  auto z2 = graph.CreateAxis("z2", s2);

  Data x("x");
  graph.AddNode(x);
  x.attr.sched.axis = {z0.id, z1.id, z2.id};
  x.y.dtype = ge::DT_FLOAT;
  *(x.y.axis) = {z0.id, z1.id, z2.id};

  Load load("load");
  graph.AddNode(load);
  load.x = x.y;
  load.attr.sched.axis = {z0.id, z1.id, z2.id};
  load.y.dtype = ge::DT_FLOAT;
  *load.y.axis = {z0.id, z1.id, z2.id};
  *load.y.repeats = {s0, s1, s2};
  *load.y.strides = {s1*s2, s2, One};

  Ub2ub ub2ub("ub2ub");
  ub2ub.x = load.y;
  ub2ub.y.dtype = ge::DT_FLOAT;
  ub2ub.attr.sched.axis = {z0.id, z1.id, z2.id};
  *ub2ub.y.repeats = {s0, s1, s2};
  *ub2ub.y.strides = {s1*s2, s2, One};
  ub2ub.attr.tmp_buffers = {{{ge::Symbol(8192), -1}, MemAttr()}};

  ge::ascir_op::Abs abs("abs");
  graph.AddNode(abs);
  abs.x = ub2ub.y;
  abs.y.dtype = ge::DT_FLOAT;
  abs.attr.sched.axis = {z0.id, z1.id, z2.id};
  *abs.y.repeats = {s0, s1, s2};
  *abs.y.strides = {s1*s2, s2, One};

  Store store("store");
  graph.AddNode(store);
  store.x = abs.y;
  store.attr.sched.axis = {z0.id, z1.id, z2.id};
  store.y.dtype = ge::DT_FLOAT;
  *store.y.axis = {z0.id, z1.id, z2.id};
  *store.y.repeats = {s0, s1, s2};
  *store.y.strides = {s1*s2, s2, One};

  Output y("y");
  graph.AddNode(y);
  y.x = store.y;
  y.attr.sched.axis = {z0.id, z1.id, z2.id};
  y.y.dtype = ge::DT_FLOAT;
  *y.y.axis = {z0.id, z1.id, z2.id};
}

void LoadUb2ubAbsStore_AfterInferOutput(ge::AscGraph &graph) {
    auto x = graph.FindNode("x");
    x->attr.api.compute_type = ComputeType::kComputeInvalid; // ComputeType::COMPUTE_DATA;

    auto load = graph.FindNode("load");
    load->outputs[0].attr.dtype = ge::DT_FLOAT;
    load->attr.api.compute_type = ComputeType::kComputeLoad;

    auto ub2ub = graph.FindNode("ub2ub");
    ub2ub->outputs[0].attr.dtype = ge::DT_FLOAT;
    ub2ub->outputs[0].attr.axis = load->outputs[0].attr.axis;
    ub2ub->outputs[0].attr.repeats = load->outputs[0].attr.repeats;
    ub2ub->outputs[0].attr.strides = load->outputs[0].attr.strides;
    ub2ub->attr.api.compute_type = ComputeType::kComputeElewise;

    auto abs = graph.FindNode("abs");
    abs->outputs[0].attr.dtype = ge::DT_FLOAT;
    abs->outputs[0].attr.axis = ub2ub->outputs[0].attr.axis;
    abs->outputs[0].attr.repeats = ub2ub->outputs[0].attr.repeats;
    abs->outputs[0].attr.strides = ub2ub->outputs[0].attr.strides;
    abs->attr.api.compute_type = ComputeType::kComputeElewise;

    auto store = graph.FindNode("store");
    store->outputs[0].attr.dtype =  ge::DT_FLOAT;
    store->attr.api.compute_type = ComputeType::kComputeStore;

    auto y = graph.FindNode("y");
    y->attr.api.compute_type = ComputeType::kComputeInvalid;
}

